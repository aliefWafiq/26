
const txtWindow = document.getElementById('txt-window')
const galleryWindow = document.getElementById('gallery-window')

function showTxt() {
    txtWindow.style.display = 'block'
}

function closeTxt() {
    txtWindow.style.display = 'none'
}

function showGallery() {
    galleryWindow.style.display = 'block'
}

function closeGallery() {
    galleryWindow.style.display = 'none'
}